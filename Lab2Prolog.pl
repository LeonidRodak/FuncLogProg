/*Определите отношение подмножество(список1, список2), который истинен,
если список1 является подмножеством списка2.*/

% Предикат contains проверяет, содержится ли элемент X в списке
% Если голова списка (H) равна искомому элементу (X) — истина
contains([H|_], H).

% Рекурсивный случай: если голова не равна X, ищем X в хвосте списка (T)
contains([_|T], X) :-
    contains(T, X).

% Предикат subset проверяет, является ли первый список подмножеством второго
% Пустой список является подмножеством любого списка
subset([], _).

% Рекурсивный случай: список [H|T] является подмножеством List2, если:
% 1. Голова (H) содержится в List2
% 2. Хвост (T) также является подмножеством List2
subset([H|T], List2) :-
    contains(List2, H),    % Проверяем текущий элемент
    subset(T, List2).      % Рекурсивно проверяем остальное


% Задание 2
/*Определите отношение линеаризация(список, линейный список), которое истинно,
если список есть список списков, а линейный список все элементы подсписков,
выписанные слева направо, например линеаризация([[1,2], [3,4], [5,6]], [1,2,3,4,5,6]) истинен.*/

% Пустой список линеаризуется в пустой список
lin([], []).

% Случай 1: Голова списка (H) сама является списком
% Пример: [[1,2], [3]] -> [1,2,3]
lin([H|T], L) :-
    is_list(H), !,           % Проверяем, что H — список. Отсечение (!) запрещает поиск других правил
    lin(H, HLin),            % Рекурсивно линеаризуем вложенный список H
    lin(T, TLin),            % Рекурсивно линеаризуем хвост T
    append(HLin, TLin, L). % Склеиваем результаты в один плоский список L и проверяем 

% Случай 2: Голова списка (H) НЕ является списком (обычный элемент)
% Пример: [1, [2,3]] -> [1,2,3]
lin([H|T], [H|L]) :-
    lin(T, L).                  % Оставляем H как есть, линеаризуем только хвост