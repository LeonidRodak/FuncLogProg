%['Lab3Prolog'].
/*Задача о миссионерах и людоедах 

На левом берегу реки n миссионеров и n людоедов, а также лодка вместимостью m мест.
Необходимо перевезти миссионеров и людоедов на другой берег, так, чтобы людоеды не съели миссионеров.
Людоеды съедают миссионеров если их находится на берегу больше по количеству
Лодкой кто-то должен управлять, поэтому высаживать всех из лодки нельзя*/

/*Структура лодки: (Управляющий, Пассажир, Пассажир)
Минимум 1 человек (управляющий обязателен)
Максимум 3 человека (1 управляющий + 2 пассажира)
Лодка не может плавать пустой!*/


% Состояние описывается как: state(ML, CL, Pos, MR, CR)
% где: ML/CL - миссионеры/людоеды слева, MR/CR - справа, Pos - позиция лодки

% Проверка безопасности
safe(state(ML, CL, _, MR, CR)) :-
    % 1. Проверка на отрицательные числа (нельзя иметь меньше 0 человек)
    ML >= 0, CL >= 0, MR >= 0, CR >= 0,
    
    % 2. Левый берег безопасен, если:
    % миссионеров нет (0), ИЛИ
    % миссионеров не меньше, чем людоедов
    (ML =:= 0; ML >= CL),
    
    % 3. Правый берег безопасен (аналогично)
    (MR =:= 0; MR >= CR).


% Цель: все люди на правом берегу
goal(state(0, 0, _, _, _)).


% move(ТекущееСостояние, НовоеСостояние, ВместимостьЛодки)

% 1. Перемещение слева направо
move(state(ML, CL, left, MR, CR), state(ML2, CL2, right, MR2, CR2), M) :-
    % Структура лодки
    % Total - общее количество людей в лодке
    % Минимум 1 (управляющий обязателен)
    % Максимум M (вместимость лодки 3)
    between(1, M, Total),
    
    % MoveM - сколько миссионеров в лодке (могут быть управляющим или пассажирами)
    between(0, Total, MoveM),
    
    % MoveC - сколько людоедов в лодке (оставшиеся места)
    MoveC is Total - MoveM,
    
    % Проверка: хватает ли людей на берегу для посадки
    MoveM =< ML,
    MoveC =< CL,
    
    % Вычисляем новое распределение людей
    ML2 is ML - MoveM,   % Миссионеры слева уменьшились
    CL2 is CL - MoveC,   % Людоеды слева уменьшились
    MR2 is MR + MoveM,   % Миссионеры справа увеличились
    CR2 is CR + MoveC,   % Людоеды справа увеличились
    
    % Проверка безопасности нового состояния
    safe(state(ML2, CL2, right, MR2, CR2)).



% 2. Перемещение справа налево
move(state(ML, CL, right, MR, CR), state(ML2, CL2, left, MR2, CR2), M) :-
    % Структура лодки
    % Минимум 1 человек (управляющий), максимум M (вместимость)
    between(1, M, Total),
    
    % Сколько миссионеров в лодке
    between(0, Total, MoveM),
    
    % Сколько людоедов в лодке
    MoveC is Total - MoveM,
    
    % Проверка: хватает ли людей на правом берегу
    MoveM =< MR,
    MoveC =< CR,
    
    % Вычисляем новое распределение (люди плывут обратно)
    ML2 is ML + MoveM,   % Миссионеры слева увеличились
    CL2 is CL + MoveC,   % Людоеды слева увеличились
    MR2 is MR - MoveM,   % Миссионеры справа уменьшились
    CR2 is CR - MoveC,   % Людоеды справа уменьшились
    
    % Проверка безопасности нового состояния
    safe(state(ML2, CL2, left, MR2, CR2)).


% Поиск пути
% path(ТекущееСостояние, Посещённые, Путь, ВместимостьЛодки)

% Достигли цели
path(State, _, [State], _) :- 
    goal(State), !.

% Рекурсивный случай: ищем следующий шаг
path(State, Visited, [State | Path], M) :-
    % Находим допустимое перемещение
    move(State, NextState, M),
    
    % Проверяем, что не были в этом состоянии (защита от циклов)
    \+ member(NextState, Visited),
    
    % Рекурсивный поиск от нового состояния
    path(NextState, [State | Visited], Path, M).


% Запуск решения
% solve(КоличествоЛюдей, ВместимостьЛодки, Путь)
solve(N, M, Path) :-
    % Условия
    N > 0,
    %N > M,
    M >= 1,
    M >= 3,
    
    % Начальное состояние: все люди слева, лодка слева
    InitialState = state(N, N, left, 0, 0),
    
    % Запускаем поиск пути
    path(InitialState, [], Path, M).

