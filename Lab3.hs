{-Функция одной переменной doItYourself выбирает наибольшее из переданного ей аргумента и 42,
 затем возводит результат выюора в куб и, наконец, вычисляет логарифм по основанию 2 от полученного числа-}
doItYourself = f . g . h  --выполняется справа налево: сначала h, потом g, потом f

f = logBase 2    -- вычисляет логарифм по основанию 2
g = (^3)         -- возводит в куб
h = max 42       -- выбирает максимум из аргумента и 42