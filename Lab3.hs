-- Задание 1
-- Реализация функции on3, аналогичной 'on', но для трёхместной функции.
on3 op f x y z = op (f x) (f y) (f z)  {-op - трёхместная функцию типа (b -> b -> b -> c)
                                        f  - функцию-преобразователь типа (a -> b)
                                        x, y, z - три аргумента типа a
                                        Применяет f к каждому из трёх аргументов, затем передаёт результаты в op-}
{-Проверка:
sum3squares = (\x y z -> x+y+z) `on3` (^2)
sum3squares 1 2 3-}

